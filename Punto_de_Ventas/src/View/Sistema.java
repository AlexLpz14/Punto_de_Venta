    void calcularTotal(){
       totalPago = 0; 
        for (int i = 0; i < TablaProd.getRowCount(); i++) {
            cant = Integer.parseInt(TablaProd.getValueAt(i, 4).toString());
            precio = Float.parseFloat(TablaProd.getValueAt(i, 2).toString());
            totalPago = totalPago+(cant*precio);
        }
        pagarLabel.setText(totalPago+"0");
    }
    public void buscarProducto(String consulta,int cantidad){
//        Productos p = new Productos();
        Conexion conex = new Conexion();
	Connection c = conex.getConn();
        try{
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery(consulta);
            if(rs == null){
                rs.close();
            }else{
                while(rs.next()){
                    prod.setCodigo(rs.getInt(1));
                    prod.setNombre(rs.getString(2));
                    prod.setPrecio(rs.getFloat(3));
                    prod.setDescripcion(rs.getString(4));
                    prod.setCantidad(cantidad);

                }
            }

            c.close();
	}catch(SQLException se){
            JOptionPane.showMessageDialog(null, se);
	}
    }
    
    

